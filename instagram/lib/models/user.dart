class User {
  final String name;
  final String userName;
  final String bio;
  final String note;
  final String imgPath;
  final String post;
  final String followers;
  final String following;

  User({
    required this.name,
    required this.userName,
    required this.bio,
    required this.note,
    required this.imgPath,
    required this.post,
    required this.followers,
    required this.following,
  });
}
